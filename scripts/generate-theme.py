def load():
    with open(PALETTE, "rb") as f:
        return tomllib.load(f)


class Theme:
    def __init__(self, data):
        self.data = data
        self.palette = data["palette"]

    def hex(self, name):
        """Resolve a raw palette color name to '#rrggbb'."""
        try:
            return self.palette[name].lower()
        except KeyError:
            raise SystemExit(f"unknown palette color: {name}")

    def role(self, name):
        """Resolve a semantic role to '#rrggbb' via the palette."""
        try:
            return self.hex(self.data["roles"][name])
        except KeyError:
            raise SystemExit(f"unknown role: {name}")

    def kde(self, name):
        """Resolve a [kde] role to '#rrggbb' via the palette."""
        try:
            return self.hex(self.data["kde"][name])
        except KeyError:
            raise SystemExit(f"unknown kde role: {name}")

    def color(self, name):
        """Resolve a name that is either a [kde] role or a raw palette color."""
        if name in self.data.get("kde", {}):
            return self.kde(name)
        return self.hex(name)

    def rgb(self, hexval):
        h = hexval.lstrip("#")
        return int(h[0:2], 16), int(h[2:4], 16), int(h[4:6], 16)

    def truecolor(self, hexval, bold=False):
        r, g, b = self.rgb(hexval)
        prefix = "1;" if bold else ""
        return f"\\u001b[{prefix}38;2;{r};{g};{b}m"


