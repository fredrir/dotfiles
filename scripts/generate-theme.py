def gen_kitty(t, changed):
    term = t.data["terminal"]
    ansi = t.data["terminal"]["ansi"]
    slot = {  # ANSI role name -> kitty colorNN index
        "black": 0, "red": 1, "green": 2, "yellow": 3, "blue": 4, "magenta": 5,
        "cyan": 6, "white": 7, "bright_black": 8, "bright_red": 9,
        "bright_green": 10, "bright_yellow": 11, "bright_blue": 12,
        "bright_magenta": 13, "bright_cyan": 14, "bright_white": 15,
    }
    lines = [
        f"# {GEN_HEADER}",
        f"# {t.data['name']}",
        "",
        f"foreground              {t.hex(term['foreground'])}",
        f"background              {t.hex(term['background'])}",
        f"selection_foreground    {t.hex(term['selection_foreground'])}",
        f"selection_background    {t.hex(term['selection_background'])}",
        f"cursor                  {t.hex(term['cursor'])}",
        f"cursor_text_color       {t.hex(term['cursor_text'])}",
        f"url_color               {t.hex(term['url'])}",
        "",
    ]
    for name, idx in slot.items():
        lines.append(f"color{idx:<2} {t.hex(ansi[name])}")
    content = "\n".join(lines) + "\n"
    write_if_changed(
        os.path.join(ROOT, "shared/kitty/.config/kitty/colors-mocha.conf"),
        content, changed,
    )


def gen_konsole(t, changed):
    term = t.data["terminal"]
    ansi = t.data["terminal"]["ansi"]
    normal = ["black", "red", "green", "yellow", "blue", "magenta", "cyan", "white"]
    bright = ["bright_" + n for n in normal]

    def rgb_str(hexval):
        return "{},{},{}".format(*t.rgb(hexval))

    def block(section, hexval):
        return [f"[{section}]", f"Color={rgb_str(hexval)}", ""]

    out = []
    out += block("Background", t.hex(term["background"]))
    out += block("BackgroundIntense", t.hex(term["background"]))
    out += block("BackgroundFaint", t.hex(term["background"]))
    for i in range(8):
        out += block(f"Color{i}", t.hex(ansi[normal[i]]))
        out += block(f"Color{i}Intense", t.hex(ansi[bright[i]]))
        out += block(f"Color{i}Faint", t.hex(ansi[normal[i]]))
    out += block("Foreground", t.hex(term["foreground"]))
    out += block("ForegroundIntense", t.hex(term["foreground"]))
    out += block("ForegroundFaint", t.hex(term["foreground"]))
    out += [
        "[General]",
        "Blur=false",
        "ColorRandomization=false",
        f"Description={t.data['name']}",
        "Opacity=1",
        "Wallpaper=",
    ]
    header = f"# {GEN_HEADER}\n"
    content = header + "\n".join(out) + "\n"
    write_if_changed(
        os.path.join(
            ROOT, "linux/kde/konsole/.local/share/konsole/Catppuccin-Mocha.colorscheme"
        ),
        content, changed,
    )


def gen_fastfetch(t, changed):
    accents = [
        ("section_system", "$1"),
        ("section_hardware", "$2"),
        ("section_desktop", "$3"),
        ("section_network", "$4"),
    ]
    const_lines = []
    for i, (role, _) in enumerate(accents):
        hx = t.role(role)
        color = t.data["roles"][role]
        const_lines.append(f'"{t.truecolor(hx, bold=True)}", // {color} {hx}')
    sep_hex = t.role("separator")
    sep_name = t.data["roles"]["separator"]
    const_lines.append(f'"{t.truecolor(sep_hex)}" // {sep_name} {sep_hex}')

    path = os.path.join(ROOT, "shared/fastfetch/.config/fastfetch/config.jsonc")

    def transform(text):
        text = replace_between(text, "constants", const_lines, indent=" " * 6)
        lines = text.split("\n")
        for idx, l in enumerate(lines):
            if "theme:separator" in l:
                lines[idx] = re.sub(r"#[0-9a-fA-F]{6}", sep_hex, l)
        return "\n".join(lines)

    edit_if_changed(path, transform, changed)
    gen_fastfetch_logo(t, changed)


def gen_fastfetch_logo(t, changed):
    """Recolor arch.txt's ASCII art with a gradient over the 4 section accents."""
    stops = [
        t.rgb(t.role("section_system")),
        t.rgb(t.role("section_hardware")),
        t.rgb(t.role("section_desktop")),
        t.rgb(t.role("section_network")),
    ]
    segments = len(stops) - 1
    path = os.path.join(ROOT, "shared/fastfetch/.config/fastfetch/arch.txt")
    with open(path, encoding="utf-8") as f:
        raw = f.read().split("\n")
    # Drop a trailing empty element from a final newline; re-add it at the end.
    trailing_nl = raw and raw[-1] == ""
    if trailing_nl:
        raw = raw[:-1]

    art = [re.sub(r"\x1b\[[0-9;]*m", "", l) for l in raw]
    n = len(art)

    def lerp(a, b, u):
        return tuple(round(a[k] + (b[k] - a[k]) * u) for k in range(3))

    out = []
    for i, line in enumerate(art):
        pos = (i / (n - 1)) * segments if n > 1 else 0
        seg = min(int(pos), segments - 1)
        r, g, b = lerp(stops[seg], stops[seg + 1], pos - seg)
        out.append(f"\x1b[1;38;2;{r};{g};{b}m{line}")
    content = "\n".join(out) + "\x1b[0m"
    if trailing_nl:
        content += "\n"
    write_if_changed(path, content, changed)


def gen_starship(t, changed):
    prompt_roles = ("prompt_python", "prompt_git", "prompt_dir", "prompt_duration", "prompt_char")
    names = list(t.palette.keys())
    width = max(len(k) for k in names + list(prompt_roles))
    lines = [f"# {GEN_HEADER}", "[palettes.mocha]"]
    for name in names:
        lines.append(f"{name.ljust(width)} = '{t.hex(name)}'")
    lines.append("")
    for role in prompt_roles:
        lines.append(f"{role.ljust(width)} = '{t.role(role)}'")

    path = os.path.join(ROOT, "shared/starship/.config/starship.toml")
    edit_if_changed(path, lambda text: replace_between(text, "palette", lines), changed)


def gen_zsh(t, changed):
    def esc(role):
        er, eg, eb = t.rgb(t.role(role))
        return f"$'\\e[38;2;{er};{eg};{eb}m'"

    lines = [
        f"# {GEN_HEADER}",
        "export THEME_RESET=$'\\e[0m'",
        f"export THEME_SUDO={esc('sudo')}",
        f"export THEME_GIT={esc('prompt_git')}",
        f"export THEME_DIR={esc('prompt_dir')}",
        f"export THEME_CHAR={esc('prompt_char')}",
    ]
    eza = dict(t.data.get("eza", {}))
    if eza:
        categories = eza.pop("categories", {})

        def ansi(color):
            er, eg, eb = t.rgb(t.hex(color))
            return f"38;2;{er};{eg};{eb}"

        parts = ["reset"]
        # file kinds (fi is the base fallback for every regular file)
        for kind in ("fi", "di", "ex", "ln", "pi", "so", "bd", "cd"):
            if kind in eza:
                parts.append(f"{kind}={ansi(eza[kind])}")
        # categories expand to a glob per extension
        for cat, color in categories.items():
            exts = EZA_CATEGORY_EXTS.get(cat)
            if exts is None:
                sys.stderr.write(f"theme: unknown [eza.categories] group '{cat}'\n")
                continue
            for ext in exts.split():
                parts.append(f"*.{ext}={ansi(color)}")
        # explicit "*.ext" overrides come last so they win
        for key, color in eza.items():
            if key.startswith("*"):
                parts.append(f"{key}={ansi(color)}")
        lines.append("unset LS_COLORS")
        lines.append(f'export EZA_COLORS="{":".join(parts)}"')
    content = "\n".join(lines) + "\n"
    write_if_changed(
        os.path.join(ROOT, "shared/zsh/.config/zsh/conf.d/03-theme.zsh"),
        content, changed,
    )


