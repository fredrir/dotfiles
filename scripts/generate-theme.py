def write_if_changed(path, content, changed):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    old = None
    if os.path.exists(path):
        with open(path, encoding="utf-8") as f:
            old = f.read()
    if old != content:
        with open(path, "w", encoding="utf-8") as f:
            f.write(content)
        changed.append(os.path.relpath(path, ROOT))


def edit_if_changed(path, transform, changed):
    with open(path, encoding="utf-8") as f:
        old = f.read()
    new = transform(old)
    if new != old:
        with open(path, "w", encoding="utf-8") as f:
            f.write(new)
        changed.append(os.path.relpath(path, ROOT))


def replace_between(text, name, new_lines, indent=""):
    """Replace the lines between `theme:<name>` and `theme:<name>:end` markers."""
    start = f"theme:{name}"
    end = f"theme:{name}:end"
    lines = text.split("\n")
    i = next((k for k, l in enumerate(lines) if end not in l and start in l), None)
    j = next((k for k, l in enumerate(lines) if end in l), None)
    if i is None or j is None or j < i:
        raise SystemExit(f"markers '{start}' / '{end}' not found in the target file")
    body = [indent + l if l else l for l in new_lines]
    return "\n".join(lines[: i + 1] + body + lines[j:])


def _ini_section_bounds(lines, header):
    """Return (start, end) line indices for a KConfig section. `start` is the
    header line; the body runs (start, end) up to the next section or EOF."""
    marker = f"[{header}]"
    start = next((k for k, l in enumerate(lines) if l == marker), None)
    if start is None:
        raise SystemExit(f"kdeglobals: section '{marker}' not found")
    end = start + 1
    while end < len(lines) and not lines[end].startswith("["):
        end += 1
    return start, end


def replace_ini_section(text, header, body_lines):
    """Replace a KConfig section's body in place, keeping its position, the rest
    of the file, and the original count of trailing blank lines."""
    lines = text.split("\n")
    start, end = _ini_section_bounds(lines, header)
    old_body = lines[start + 1:end]
    blanks = 0
    while old_body and old_body[-1] == "":
        blanks += 1
        old_body.pop()
    new_body = list(body_lines) + [""] * blanks
    return "\n".join(lines[:start + 1] + new_body + lines[end:])


def set_ini_key(text, header, key, value):
    """Set `key=value` within a KConfig section, inserting in alphabetical order
    if the key is absent."""
    lines = text.split("\n")
    start, end = _ini_section_bounds(lines, header)
    for k in range(start + 1, end):
        if lines[k].split("=", 1)[0] == key:
            lines[k] = f"{key}={value}"
            return "\n".join(lines)
    insert_at = end
    for k in range(start + 1, end):
        name = lines[k].split("=", 1)[0]
        if name and not lines[k].startswith("[") and name > key:
            insert_at = k
            break
    lines.insert(insert_at, f"{key}={value}")
    return "\n".join(lines)


