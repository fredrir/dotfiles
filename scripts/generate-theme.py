def gen_kde_colorscheme(t, changed):
    """Stamp the KDE/Qt color scheme into kdeglobals ([Colors:*], [WM], accent)."""
    def rgb_str(role):
        return "{},{},{}".format(*t.rgb(t.kde(role)))

    sections = {}
    for group, (bg, alt) in KDE_GROUPS.items():
        overrides = KDE_SELECTION_FG if group == "Colors:Selection" else {}
        body = [
            f"BackgroundAlternate={rgb_str(alt)}",
            f"BackgroundNormal={rgb_str(bg)}",
            f"DecorationFocus={rgb_str('decoration')}",
            f"DecorationHover={rgb_str('decoration')}",
        ]
        for key, role in KDE_FOREGROUNDS:
            body.append(f"{key}={rgb_str(overrides.get(key, role))}")
        sections[group] = body
    sections["WM"] = [
        f"activeBackground={rgb_str('wm_active_bg')}",
        f"activeBlend={rgb_str('wm_active_blend')}",
        f"activeForeground={rgb_str('wm_active_fg')}",
        f"inactiveBackground={rgb_str('wm_inactive_bg')}",
        f"inactiveBlend={rgb_str('wm_inactive_blend')}",
        f"inactiveForeground={rgb_str('wm_inactive_fg')}",
    ]

    path = os.path.join(ROOT, "linux/kde/plasma/.config/kdeglobals")

    def transform(text):
        for header, body in sections.items():
            text = replace_ini_section(text, header, body)
        text = set_ini_key(text, "General", "AccentColor", rgb_str("accent"))
        text = set_ini_key(text, "General", "LastUsedCustomAccentColor", rgb_str("accent"))
        return text

    edit_if_changed(path, transform, changed)


def gen_gtk(t, changed):
    """Rewrite the GTK colors.css @define-color values from GTK_MAP so GTK apps
    match the KDE scheme."""
    line_re = re.compile(r"(@define-color\s+)(\S+)(\s+)(#[0-9a-fA-F]{6,8})(;.*)$")

    def transform(text):
        out = []
        for line in text.split("\n"):
            m = line_re.match(line)
            if m:
                var = m.group(2)
                base = var[:-7] if var.endswith("_breeze") else var
                if base in GTK_MAP:
                    line = f"{m.group(1)}{var}{m.group(3)}{t.color(GTK_MAP[base])}{m.group(5)}"
                else:
                    sys.stderr.write(f"theme: unmapped GTK color '{var}'\n")
            out.append(line)
        return "\n".join(out)

    for ver in ("gtk-3.0", "gtk-4.0"):
        edit_if_changed(
            os.path.join(ROOT, f"linux/common/gtk/.config/{ver}/colors.css"),
            transform, changed,
        )


def gen_kde_widgets(t, changed):
    """Retag Catppuccin colors embedded in KDE widget config (panel-colorizer
    presets and the desktop-appletsrc) so they track the palette."""
    hex_map = {src: t.hex(role) for src, role in CATPPUCCIN_TO_PALETTE.items()}
    rgb_map = {}
    for src, role in CATPPUCCIN_TO_PALETTE.items():
        key = "{},{},{}".format(*t.rgb("#" + src))
        rgb_map[key] = "{},{},{}".format(*t.rgb(t.hex(role)))

    hex_re = re.compile(r"#([0-9a-fA-F]{8}|[0-9a-fA-F]{6})")
    rgb_line_re = re.compile(r"^([^=\[]+)=(\d{1,3},\d{1,3},\d{1,3})$")

    def remap_hex(text):
        def repl(m):
            tok = m.group(1).lower()
            return hex_map[tok] if len(tok) == 6 and tok in hex_map else m.group(0)
        return hex_re.sub(repl, text)

    presets = os.path.join(
        ROOT, "linux/kde/panel-colorizer/.config/panel-colorizer/presets")
    if os.path.isdir(presets):
        for name in sorted(os.listdir(presets)):
            p = os.path.join(presets, name, "settings.json")
            if os.path.isfile(p) and os.path.getsize(p):
                edit_if_changed(p, remap_hex, changed)

    def remap_appletsrc(text):
        out = []
        for line in remap_hex(text).split("\n"):
            m = rgb_line_re.match(line)
            if m and m.group(2) in rgb_map:
                out.append(f"{m.group(1)}={rgb_map[m.group(2)]}")
            else:
                out.append(line)
        return "\n".join(out)

    edit_if_changed(
        os.path.join(ROOT, "linux/kde/plasma/.config/plasma-org.kde.plasma.desktop-appletsrc"),
        remap_appletsrc, changed,
    )


def main():
    t = Theme(load())
    changed = []
    gen_kitty(t, changed)
    gen_konsole(t, changed)
    gen_fastfetch(t, changed)
    gen_starship(t, changed)
    gen_zsh(t, changed)
    gen_kde_colorscheme(t, changed)
    gen_gtk(t, changed)
    gen_kde_widgets(t, changed)
    if changed:
        print("theme: regenerated")
        for p in changed:
            print(f"  {p}")
    else:
        print("theme: already up to date")
    return 0


if __name__ == "__main__":
    sys.exit(main())
